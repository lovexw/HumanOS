// Modal functionality
const modal = document.getElementById('organModal');
const modalBody = document.getElementById('modalBody');
const closeBtn = document.getElementsByClassName('close')[0];

function showOrganDetail(organId) {
    const organ = organData[organId];
    if (!organ) return;

    const detailHTML = `
        <div class="modal-header">
            <span class="close">&times;</span>
            <div class="modal-organ-icon">${organ.icon}</div>
            <h2 class="modal-organ-name">${organ.name}</h2>
            <p class="modal-organ-subtitle">${organ.subtitle}</p>
        </div>
        <div class="modal-body">
            <div class="detail-section">
                <h3>🔬 主要功能</h3>
                <p>${organ.functions}</p>
            </div>
            <div class="detail-section">
                <h3>💚 保养方法</h3>
                <ul>
                    ${organ.care.map(item => `<li>${item}</li>`).join('')}
                </ul>
            </div>
            <div class="detail-section">
                <h3>⚠️ 避免伤害</h3>
                <div class="warning-box">
                    <h4>请注意以下事项，保护您的${organ.name}健康：</h4>
                    <ul>
                        ${organ.avoid.map(item => `<li>${item}</li>`).join('')}
                    </ul>
                </div>
            </div>
        </div>
    `;

    modalBody.innerHTML = detailHTML;
    modal.style.display = 'block';
    document.body.style.overflow = 'hidden';

    // Re-attach close button event
    const newCloseBtn = modalBody.querySelector('.close');
    newCloseBtn.onclick = closeModal;
}

function closeModal() {
    modal.style.display = 'none';
    document.body.style.overflow = 'auto';
}

// Close modal when clicking outside
window.onclick = function(event) {
    if (event.target === modal) {
        closeModal();
    }
}

// Filter functionality
const filterButtons = document.querySelectorAll('.filter-btn');
const organCards = document.querySelectorAll('.organ-card');

filterButtons.forEach(button => {
    button.addEventListener('click', () => {
        // Update active button
        filterButtons.forEach(btn => btn.classList.remove('active'));
        button.classList.add('active');

        const category = button.getAttribute('data-category');

        // Filter cards
        organCards.forEach(card => {
            if (category === 'all' || card.getAttribute('data-category') === category) {
                card.style.display = 'block';
                card.style.animation = 'fadeInUp 0.6s ease forwards';
            } else {
                card.style.display = 'none';
            }
        });
    });
});

// Smooth scroll for navigation
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Active nav link on scroll
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-link');

window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.clientHeight;
        if (pageYOffset >= sectionTop - 200) {
            current = section.getAttribute('id');
        }
    });

    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

// Add animation on scroll
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, observerOptions);

